#!/usr/bin/env node
// scripts/setup-wizard/server.js
// Web-based setup wizard with OAuth callbacks and credential validation.
// Uses only built-in Node.js modules — no Express or other dependencies.

import { createServer } from 'http';
import { readFileSync, existsSync, writeFileSync } from 'fs';
import { join, dirname, extname } from 'path';
import { fileURLToPath } from 'url';
import { execSync } from 'child_process';
import { arch } from 'os';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..', '..');
const PUBLIC_DIR = join(__dirname, 'public');
const ENV_PATH = join(ROOT, '.env');
const PORT = 3456;

// ── MIME types ──

const MIME = {
  '.html': 'text/html',
  '.css': 'text/css',
  '.js': 'application/javascript',
  '.json': 'application/json',
  '.png': 'image/png',
  '.svg': 'image/svg+xml',
};

// ── Helpers ──

function loadExistingEnv() {
  if (!existsSync(ENV_PATH)) return {};
  const env = {};
  for (const line of readFileSync(ENV_PATH, 'utf8').split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq > 0) env[trimmed.slice(0, eq)] = trimmed.slice(eq + 1);
  }
  return env;
}

function detectPuppeteerPath() {
  const cpuArch = arch();
  if (cpuArch === 'arm64' || cpuArch === 'arm') {
    for (const p of ['/usr/bin/chromium-browser', '/usr/bin/chromium', '/snap/bin/chromium']) {
      if (existsSync(p)) return p;
    }
    return '/usr/bin/chromium-browser';
  }
  return '';
}

function defaultExpiryDate() {
  const d = new Date();
  d.setDate(d.getDate() + 60);
  return d.toISOString().split('T')[0];
}

